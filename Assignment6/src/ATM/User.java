package ATM;

class User {
	String name;
	int age;
	String address;
	String phoneNumber;
	String bankAccountNumber;
	
	User(String name, int age, String address, String phoneNumber, String bankAccountNumber){
		this.name = name;
		this.age = age;
		this.address = address;
		this.phoneNumber = phoneNumber;
		this.bankAccountNumber = bankAccountNumber;		
	}
}
